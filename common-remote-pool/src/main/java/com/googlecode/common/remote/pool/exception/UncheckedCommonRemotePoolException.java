package com.googlecode.common.remote.pool.exception;

public class UncheckedCommonRemotePoolException extends RuntimeException {

	/**
     *
     */
	private static final long serialVersionUID = 1L;

	public UncheckedCommonRemotePoolException() {
		super();
	}

	public UncheckedCommonRemotePoolException(String message, Throwable cause) {
		super(message, cause);
	}

	public UncheckedCommonRemotePoolException(String message) {
		super(message);
	}

	public UncheckedCommonRemotePoolException(Throwable cause) {
		super(cause);
	}

}
