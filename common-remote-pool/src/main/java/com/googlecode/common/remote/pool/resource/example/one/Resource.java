package com.googlecode.common.remote.pool.resource.example.one;


public class Resource {

	private String file;


	public Resource(){

	}

    public Resource(String file) {
        super();
        this.file = file;
    }


    public String getFile() {
        return file;
    }


    public void setFile(String file) {
        this.file = file;
    }

}