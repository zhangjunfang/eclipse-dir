package cn.newcapec.framework.core.rest;

/**
 * @author: andy.li
 * 
 */
public class MetaData {

	private String messageProperty = "msg";

	private String successProperty = "success";

	/**
	 * @return the messageProperty
	 */
	public String getMessageProperty() {
		return messageProperty;
	}

	/**
	 * @param messageProperty
	 *            the messageProperty to set
	 */
	public void setMessageProperty(String messageProperty) {
		this.messageProperty = messageProperty;
	}

	/**
	 * @return the successProperty
	 */
	public String getSuccessProperty() {
		return successProperty;
	}

	/**
	 * @param successProperty
	 *            the successProperty to set
	 */
	public void setSuccessProperty(String successProperty) {
		this.successProperty = successProperty;
	}

}
